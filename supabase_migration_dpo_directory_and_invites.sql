--
-- What you get:
-- - district_dpo_directory: editable mapping of district -> DPO email
-- - dpo_invites + RPCs: create invite links, and accept invite links (creates an assessment row)
--
-- Notes:
-- - This migration assumes you already have the `assessments` table and the reporting-period columns migration applied.
-- - This migration uses a small `system_admins` table so you can control who can generate invites.

